    public  boolean verif()
    {
        
        if (jTextField_ID.getText().equals("")||jTextField_vorname.getText().equals("")||jTextField_nachname.getText().equals("")
                || jDateChooser_datum.getDate()== null || jTextField_service.getText().equals("")||jTextField_daure.getText().equals("")
                ||!(((jRadioButton_weiblich.isSelected()) ||(jRadioButton_mannlich.isSelected())))  )
        {
           
            return false;
        }
        else
        {
        return true;
        }
    }
    public void clear()
    {
        jTextField_ID.setText("");
        jTextField_vorname.setText("");
         jTextField_nachname.setText("");
         jTextField_service.setText("");
         jTextField_daure.setText("");
        jDateChooser_datum.setDate(null);
        jRadioButton_weiblich.setSelected(false) ;
        jRadioButton_mannlich.setSelected(false); 
        
           
        
        
    }
