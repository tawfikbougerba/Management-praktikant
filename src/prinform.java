    private void table1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table1MouseClicked
       int rowindex=table1.getSelectedRow();
       DefaultTableModel md = (DefaultTableModel) table1.getModel();
       if(md.getValueAt(rowindex,6).toString().equals("männlich"))
       {
           jRadioButton_mannlich.setSelected(true);
           jRadioButton_weiblich.setSelected(false);
           
       }else
       {
           jRadioButton_mannlich.setSelected(false);
           jRadioButton_weiblich.setSelected(true);
       }
       jTextField_ID.setText(md.getValueAt(rowindex, 0).toString());
       jTextField_vorname.setText(md.getValueAt(rowindex, 1).toString());
       jTextField_nachname.setText(md.getValueAt(rowindex, 2).toString());
       jTextField_service.setText(md.getValueAt(rowindex, 4).toString());
       jTextField_daure.setText(md.getValueAt(rowindex, 5).toString());
       
       Date datum;
       try {
           datum =new SimpleDateFormat("yyyy-MM-dd").parse(md.getValueAt(rowindex,3).toString());
           jDateChooser_datum.setDate(datum);
       } catch (Exception e) {
           System.out.println(e);
       }
       
       
    }//GEN-LAST:event_table1MouseClicked

