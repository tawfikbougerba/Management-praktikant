    private void button_einfugenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_einfugenActionPerformed

        
         if(verif())
           {
        int  id = Integer.valueOf(jTextField_ID.getText()); 
           
           String vorname =jTextField_vorname.getText() ;
           String nachname =jTextField_nachname.getText() ;
                   
          String service =jTextField_service.getText() ;
          String daure =jTextField_daure.getText() ;
         
           
                String gesch ="weiblich";
           if (jRadioButton_mannlich.isSelected())
                {
                    gesch ="männlich";
                }
                SimpleDateFormat dateformt = new SimpleDateFormat("yyyy-MM-dd");
                String Datum = dateformt.format(jDateChooser_datum.getDate());
                pr.Inser(id, vorname, nachname, Datum, service, daure, gesch);
                pr.display(table1);
                clear();
           }
           else
        {
            JOptionPane.showMessageDialog(null,"Sie müssen alle felder ausfüllen");
        }
               
        
    }//GEN-LAST:event_button_einfugenActionPerformed

